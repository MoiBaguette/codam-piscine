char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		i = 0;
		while (str[j] == to_find[i])
		{
			j++;
			i++;
			if (to_find[i] == '\0')
				return (str + j - i);
		}
		j++;
	}
	return (0);
}
